# 2. 元组应用
# (1) 创建10个餐厅菜单项目(菜名)，作为元组menu中的元素
menu = ('宫保鸡丁', '鱼香肉丝', '麻婆豆腐', '红烧肉', '水煮鱼', '回锅肉', '糖醋排骨', '夫妻肺片', '口水鸡', '酸菜鱼')
print("元组:", menu)
# 输出: 元组: ('宫保鸡丁', '鱼香肉丝', '麻婆豆腐', '红烧肉', '水煮鱼', '回锅肉', '糖醋排骨', '夫妻肺片', '口水鸡', '酸菜鱼')

# (2) 输出索引位置5和最末位上的项目
print(f"索引位置5: {menu[5]}")
# 输出: 索引位置5: 回锅肉
print(f"最末位: {menu[-1]}")
# 输出: 最末位: 酸菜鱼

# (3) 取出索引为2到7的元素
# 索引2到7（包含7），切片为 [2:8]
slice_items = menu[2:8]
print("索引2到7的元素:", slice_items)
# 输出: 索引2到7的元素: ('麻婆豆腐', '红烧肉', '水煮鱼', '回锅肉', '糖醋排骨', '夫妻肺片')

# (4) 删除元组
del menu
try:
    print(menu)
except NameError:
    print("元组已删除")
    # 输出: 元组已删除
